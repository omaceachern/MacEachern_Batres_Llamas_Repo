package MacEachern_Batres_Llamas_Repo;
public class Students {
    private String name;
    private int grade;
    public Students(String name, int grade) {
        this.name = name;
        this.grade = grade;
    }
    public String getName() {
        return name;
    }
    public int getGrade() {
        return grade;
    }
    
}